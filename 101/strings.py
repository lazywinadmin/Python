# New line
print("Hello\nWorld!")

# Escape characters
print("Hello \"World!\"")

# Concat
phrase="Hello"
print(phrase + " World!")

# Using function/methods
print(phrase.lower())
print(phrase.upper())
print(phrase.isupper())
print(phrase.upper().isupper())
print(len(phrase)) # lenght

# index
print(phrase[1]) # grab one of the character
print(phrase.index("H")) #find the index for an item

# Replace
phrase="Hello people of the "
print(phrase.replace("Hello","Bonjour"))




